var calculator = {
  add: function(add1, add2) {
    return add1 + add2
  },
  subtract: function(subt1, subt2) {
    return subt1 - subt2
  },
  multiply: function(mult1, mult2) {
    return mult1 * mult2;
  },
  divide: function(div1, div2) {
    return div1 / div2;
  },
  exponent: function(exp1, exp2) {
    return Math.pow(exp1, exp2);
  },
  maxi: function(m1, m2) {
    return Math.max(m2, m2)
  }
}
